    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        if(txt_id.getText().equals("")){
              JOptionPane.showMessageDialog(this, "Text Id Kosong","Informasi",1);
              txt_id.requestFocus();
          }
          else{
          
        Connection c=buka_koneksi();
         String sqlkode="Delete from pinjam "+ "Where Id='"+this.txt_id.getText()+"'";
            try {
               PreparedStatement p2=(PreparedStatement) c.prepareStatement(sqlkode);
               p2.executeUpdate();
               p2.close();
               
               
                txt_id.setText("");
                txt_nm.setText("");
                txt_kode.setText("");
                txt_judul.setText("");
                txt_pinjam.setText("");
                txt_kmbli.setText("");
                txt_denda.setText("");
                txt_id.setEnabled(true);
                txt_kode.setEnabled(true);
                txt_id.requestFocus();
               tampilkandata();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
       if((txt_id.getText().equals(""))||(txt_kode.getText().equals(""))||(txt_judul.getText().equals(""))
                 ||(txt_pinjam.getText().equals(""))||(txt_kmbli.getText().equals(""))||(txt_denda.getText().equals("")))
            {
                JOptionPane.showMessageDialog(this, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
            }
        else{
        Connection c=buka_koneksi(); 
        String sqlkode="Update pinjam SET Kode='"+this.txt_kode.getText()+"'," + "Tgl_Pinjam='"+this.txt_pinjam.getText()+"',"
                +"Tgl_Kembali='"+this.txt_kmbli.getText()+"',"+"Denda='"+this.txt_denda.getText()+"'"+"Where ID='"+this.txt_id.getText()+"'";
            try {
               PreparedStatement p2=(PreparedStatement) c.prepareStatement(sqlkode);
               p2.executeUpdate();
               p2.close();
               JOptionPane.showMessageDialog(this, "Edit Berhasil","Informasi",1);
               txt_id.setText("");
               txt_nm.setText("");
               txt_pinjam.setText("");
               txt_kmbli.setText("");
               txt_kode.setText("");
               txt_judul.setText("");
               txt_denda.setText("");
               txt_id.requestFocus();
               txt_id.setEnabled(true);
               txt_kode.setEnabled(true);
               
               
               
               tampilkandata();
            }
             catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_btn_editActionPerformed

