    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        if((txt_id.getText().equals(""))||(txt_nm.getText().equals(""))||(txt_kode.getText().equals(""))||(txt_judul.getText().equals(""))
            ||(txt_pinjam.getText().equals(""))||(txt_kmbli.getText().equals(""))||(txt_denda.getText().equals("")))
        {
            JOptionPane.showMessageDialog(btn_simpan, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
        }
        else
        {
            Connection c=buka_koneksi(); //panggil function koneksi
            String sqlkode="Insert into pinjam(Id,Kode,Tgl_pinjam,Tgl_kembali,Denda) values ('"+this.txt_id.getText()+"',"
            + "'"+this.txt_kode.getText()+"',"+ "'"+this.txt_pinjam.getText()+"',"+"'"+this.txt_kmbli.getText()+"',"
            +"'"+this.txt_denda.getText()+"')";
            try { //jalankan query tersebut
                PreparedStatement p2=(PreparedStatement) c.prepareStatement(sqlkode);
                p2.executeUpdate();
                p2.close();

                //tampilkan data
                tampilkandata();
                JOptionPane.showMessageDialog(this, "Simpan Berhasil","Informasi",1);

                txt_id.setText("");
                txt_nm.setText("");
                txt_kode.setText("");
                txt_judul.setText("");
                txt_pinjam.setText("");
                txt_kmbli.setText("");
                txt_denda.setText("");
                txt_id.setEnabled(true);
                txt_kode.setEnabled(true);
                btn_hapus.setEnabled(true);
                btn_edit.setEnabled(true);

                txt_id.requestFocus();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
                txt_id.setText("");

                txt_id.setEnabled(true);
                txt_kode.setEnabled(true);
                btn_hapus.setEnabled(true);
                btn_edit.setEnabled(true);
                txt_nm.setText("");
                txt_judul.setText("");
                txt_kode.setText("");

                txt_id.requestFocus();

            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        if(txt_id.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Text ID Kosong","Informasi",1);
            txt_id.requestFocus();
        }
        else{
            DefaultTableModel tabelpinjam = new DefaultTableModel();
            tabelpinjam.addColumn("ID");
            tabelpinjam.addColumn("Kode");
            tabelpinjam.addColumn("Tgl_Pinjam");
            tabelpinjam.addColumn("Tgl_Kembali");
            tabelpinjam.addColumn("Denda");

            try {
                buka_koneksi();
                String sql = "select * from pinjam where Id="+txt_id.getText();
                Statement stat = koneksi.createStatement();
                ResultSet res=stat.executeQuery(sql);
                while (res.next()) {
                    tabelpinjam.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
                }
                table3.setModel(tabelpinjam);
                txt_id.setText("");
                txt_nm.setText("");
                txt_kode.setText("");
                txt_judul.setText("");
                txt_pinjam.setText("");
                txt_kmbli.setText("");
                txt_denda.setText("");
                txt_id.setEnabled(true);
                txt_kode.setEnabled(true);
                txt_id.requestFocus();
            } catch (Exception e) {

                JOptionPane.showMessageDialog(this, "Data Tidak Ditemukan "+e);

            }
        }
    }//GEN-LAST:event_btn_cariActionPerformed

