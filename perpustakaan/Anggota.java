    private void btn_simActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simActionPerformed
        // TODO add your handling code here:
        
        if((txt_id.getText().equals(""))||(txt_nama.getText().equals(""))||(txt_alamt.getText().equals(""))||(txt_ttl.getText().equals(""))
                 ||(txt_pekerjaan.getText().equals("")))
            {
                JOptionPane.showMessageDialog(btn_sim, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
            }
            else 
            {
                Connection c=buka_koneksi(); //panggil function koneksi
         String sqlkode="Insert into anggota(Id,Nama,Alamat,Ttl,Jkl,Pekerjaan) values ('"+this.txt_id.getText()+"',"
                     + "'"+this.txt_nama.getText()+"',"+ "'"+this.txt_alamt.getText()+"',"+"'"+this.txt_ttl.getText()+"',"
                 +"'"+this.cmb_jenis.getSelectedItem()+"',"+"'"+this.txt_pekerjaan.getText()+"')";
            try { //jalankan query tersebut
               PreparedStatement p2=(PreparedStatement) c.prepareStatement(sqlkode);
               p2.executeUpdate();
               p2.close();
               
               //tampilkan data
                tampilkandata();
                JOptionPane.showMessageDialog(this, "Simpan Berhasil","Informasi",1);
                cmb_jenis.setSelectedItem("Laki-Laki");
                txt_id.setText("");
                txt_nama.setText("");
                txt_alamt.setText("");
                txt_ttl.setText("");
                txt_pekerjaan.setText("");
                
                btn_hapus.setEnabled(true);
                btn_edit.setEnabled(true);
                
                txt_id.requestFocus();
           
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
            }
            }
    }//GEN-LAST:event_btn_simActionPerformed

