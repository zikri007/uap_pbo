    private void btn_CariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CariActionPerformed
        // TODO add your handling code here:
        if(txt_id.getText().equals("")){
              JOptionPane.showMessageDialog(this, "Text ID Kosong","Informasi",1);
              txt_id.requestFocus();
          }
        else{
        DefaultTableModel tabelanggota = new DefaultTableModel();
        tabelanggota.addColumn("Id");
        tabelanggota.addColumn("Nama");
        tabelanggota.addColumn("Alamat");
        tabelanggota.addColumn("TTL");
        tabelanggota.addColumn("JKL");
        tabelanggota.addColumn("Pekerjaan");
        try {
            buka_koneksi();
            String sql = "Select * from anggota where Id like '%" +txt_id.getText()+
                    "%'" +"or Nama like '%" + txt_id.getText() + "%'"; 
            Statement stat = koneksi.createStatement();
            ResultSet res=stat.executeQuery(sql);
            
            cmb_jenis.setSelectedItem("Laki-Laki");
            txt_id.setText("");
            txt_nama.setText("");
            txt_alamt.setText("");
            txt_ttl.setText("");
            txt_pekerjaan.setText("");
            btn_hapus.setEnabled(true);
            btn_edit.setEnabled(true);
            txt_id.requestFocus();
            while (res.next()) {
                tabelanggota.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
            }
           table.setModel(tabelanggota);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Tidak Ditemukan ");
        }
        }
    }//GEN-LAST:event_btn_CariActionPerformed

